package TwoDArray;

public class JaggedArray {
    public static void main(String[] args) {

        //Jagged Array
        int mat[][]={{1,2,3,4,5},{8,9,10},{3,4,5,5},};
        for (int i = 0; i < mat.length; i++) {
            for (int j = 0; j < mat[i].length; j++) {
                System.out.print(mat[i][j]+" ");
            }
            System.out.println();
        }





    }
}
